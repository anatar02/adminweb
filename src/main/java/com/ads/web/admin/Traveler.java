package com.ads.web.admin;

public class Traveler {
	private String travelersDepartment;
	private String travelersPurpose;
	private String travelersFromDate;
	private String travelersToDate;
	public String getTravelersDepartment() {
		return travelersDepartment;
	}
	public void setTravelersDepartment(String travelersDepartment) {
		this.travelersDepartment = travelersDepartment;
	}
	public String getTravelersPurpose() {
		return travelersPurpose;
	}
	public void setTravelersPurpose(String travelersPurpose) {
		this.travelersPurpose = travelersPurpose;
	}
	public String getTravelersFromDate() {
		return travelersFromDate;
	}
	public void setTravelersFromDate(String travelersFromDate) {
		this.travelersFromDate = travelersFromDate;
	}
	public String getTravelersToDate() {
		return travelersToDate;
	}
	public void setTravelersToDate(String travelersToDate) {
		this.travelersToDate = travelersToDate;
	}

	
}
